import streamlit as st

st.set_page_config(page_title="このアプリについて", page_icon="📄")

st.title("📄 このアプリについて")
st.write("このページも、`pages`フォルダの中にある別のPythonスクリプトです。")

st.markdown("---")

st.header("目的")
st.write(
    """
    このサンプルアプリは、Streamlitのマルチページ機能を実証するために作成されました。
    1つのStreamlitアプリとしてデプロイしながら、内部的に複数のページやツールを持つことができます。
    これにより、「非公開アプリは1つまで」という制限があるホスティングサービスでも、
    実質的に複数の機能を提供することが可能になります。
    """
)

st.header("使い方")
st.write(
    """
    1. 左のサイドバーに表示されているページ名をクリックします。
    2. ページの内容が即座に切り替わります。
    3. 各ページは独立したPythonファイルであり、それぞれが独自のロジックとUIを持つことができます。
    """
)